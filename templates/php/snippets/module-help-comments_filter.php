<h5>What is the Comments SPAM Protection module?</h5>
<p>Shield will completely block 100% of automated spam-bot comments.</p>
<p>Unlike Akismet, it does not send your site comments offsite for processing with a 3rd party.</p>
<p>There is just no need for that - we respect our own, and your own, privacy too much to tolerate that sort
   of nonsense.</p>
<dl>
	<dt>How does the SPAM Bot protection work?</dt>
	<dd>
		<p>It's a simple yet highly effective system for detecting an automatic Bot comment.</p>
		<p>Each time a visitor loads a page with a comments area, the visitor uses a hidden comment token
		   to post a new comment.</p>
		<p>SPAM bots don't use these special tokens so we can detect this and reject their comments as SPAM.</p>
		<p>The timeouts on the settings pages (cooldown and expiration) are for the hidden comments tokens and are
		   necessary for the system to work - you can normally leave these at their default values.</p>
	</dd>
	<dt>What is the human SPAM option?</dt>
	<dd>
		<p>The vast majority of comment spam is automated - bots (see above).</p>
		<p>But you get random human spam comments too which can easily by-pass SPAM filters. This is
		   natural, because they're real humans. And humans know how to click checkboxes.
		</p>
		<p>This is what the human SPAM detection is for. It uses a dictionary of common human spam terms and if
		   it matches them, it'll flag it as spam and then you can
		   review it. It helps with the decision process.
		</p>
		<p>Of course, it's not perfect. But then nothing out there is 100% guaranteed for human spammers.
		   That's why spam is a problem.
		</p>
	</dd>
</dl>